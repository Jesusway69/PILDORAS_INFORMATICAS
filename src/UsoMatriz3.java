
public class UsoMatriz3 {

    public static void main(String[] args) {
        int[][] m = {
            {10, 15, 18, 19, 21},
            {5, 25, 36, 23, 10},
            {18, 11, 3, 29, 23},
            {36, 25, 28, 39, 1},};

        for (int[] f : m) {
            System.out.println();
            for (int z : f) {

                System.out.println(z + " ");

            }
        }

    }

}
