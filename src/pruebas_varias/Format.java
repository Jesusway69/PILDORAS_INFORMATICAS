package pruebas_varias;

public class Format {

  public static void main(String[] args) {

    System.out.printf("%-10s %-10s\n", "English", "Español");
    System.out.printf("%-30s\n", "********************");
    System.out.printf("%-10s %-10s\n", "Car", "Coche");
    System.out.printf("%-10s %-10s\n", "Bird", "Pájaro");
    System.out.printf("%-10s %-10s\n", "Sky", "Cielo");
    System.out.printf("%-10s %-10s\n", "Computer", "Ordenador");
    System.out.printf("%-10s %-10s\n", "House", "Casa");
    System.out.printf("%-10s %-10s\n", "Road", "Camino");
    System.out.printf("%-10s %-10s\n", "Flag", "Bandera");

    System.out.println("Una nota corchea: \u266A");
    System.out.println("Dos notas corcheas: \u266B");
    System.out.println("Dos notas semicorcheas: \u266C");
    System.out.println("Símbolo bemol: \u266D");
    System.out.println("Símbolo sostenido: \u266F");
    System.out.println("Símbolo becuadro: \u266E");
    System.out.println("Clave de fa: \\1D122");
    System.out.println("Clave de sol: \\1D11E");

  }

}
